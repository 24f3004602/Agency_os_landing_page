# ── Build the LangGraph ──────────────────────────────────────────────────────

def build_onboarding_graph() -> StateGraph:
    """
    Assembles the onboarding StateGraph.
    Linear flow: brief → email → crm → notify → END
    Each node is async and writes its own step log.
    """
    graph = StateGraph(OnboardingState)

    graph.add_node("generate_brief", generate_brief_node)
    graph.add_node("send_email", send_email_node)
    graph.add_node("update_crm", update_crm_node)
    graph.add_node("notify_am", notify_am_node)

    graph.set_entry_point("generate_brief")
    graph.add_edge("generate_brief", "send_email")
    graph.add_edge("send_email", "update_crm")
    graph.add_edge("update_crm", "notify_am")
    graph.add_edge("notify_am", END)

    return graph.compile()


# ── Runner function called by API and Celery ─────────────────────────────────

async def run_onboarding_agent(
    flow_id: str,
    agency_id: str,
    client_id: str,
    client_name: str,
    client_email: str,
    account_manager_name: str,
    deal_notes: str = "",
) -> dict:
    """
    Runs the full onboarding graph for a client.
    Returns the final state dict.
    """
    graph = build_onboarding_graph()

    initial_state: OnboardingState = {
        "flow_id": flow_id,
        "agency_id": agency_id,
        "client_id": client_id,
        "client_name": client_name,
        "client_email": client_email,
        "account_manager_name": account_manager_name,
        "deal_notes": deal_notes,
        "client_brief": "",
        "email_sent": False,
        "crm_updated": False,
        "am_notified": False,
        "errors": [],
    }

    final_state = await graph.ainvoke(initial_state)
    return final_state