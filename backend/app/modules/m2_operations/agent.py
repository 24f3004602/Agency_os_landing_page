# ── Node 3: Update CRM ───────────────────────────────────────────────────────

async def update_crm_node(state: OnboardingState) -> OnboardingState:
    """
    Updates the client record in platform DB.
    Sets status to active and stores the generated brief.
    In production this also writes back to HubSpot.
    """
    from app.database import AsyncSessionLocal
    from app.modules.m2_operations.models import OnboardingStep, OnboardingFlow
    from app.modules.people_and_tenant.users.models import Client
    from sqlalchemy import select

    logger.info("[Onboarding] Updating CRM for client %s", state["client_id"])

    node_status = "success"
    error_msg = None

    try:
        async with AsyncSessionLocal() as db:
            client_result = await db.execute(
                select(Client).where(Client.id == uuid.UUID(state["client_id"]))
            )
            client = client_result.scalar_one_or_none()
            if client:
                client.status = "active"
                client.notes = (client.notes or "") + f"\n\n[Onboarding Brief]\n{state['client_brief']}"
                await db.commit()

    except Exception as e:
        error_msg = str(e)
        node_status = "failed"
        state["errors"].append(f"update_crm: {error_msg}")
        logger.error("[Onboarding] CRM update failed: %s", e)

    async with AsyncSessionLocal() as db:
        step = OnboardingStep(
            flow_id=uuid.UUID(state["flow_id"]),
            step_name="update_crm",
            status=node_status,
            output="Client status set to active" if node_status == "success" else None,
            error=error_msg,
        )
        db.add(step)

        flow_result = await db.execute(
            select(OnboardingFlow).where(
                OnboardingFlow.id == uuid.UUID(state["flow_id"])
            )
        )
        flow = flow_result.scalar_one_or_none()
        if flow:
            flow.last_completed_node = "update_crm"
        await db.commit()

    state["crm_updated"] = node_status == "success"
    return state


