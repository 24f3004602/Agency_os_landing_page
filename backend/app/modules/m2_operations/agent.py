"""
M2 — Onboarding Orchestration Agent

LangGraph StateGraph with four nodes:
  1. generate_brief   — Claude reads deal notes, generates a structured client brief
  2. send_email       — Drafts and sends welcome email to client via Gmail
  3. update_crm       — Updates client record in platform DB
  4. notify_am        — Sends Slack notification to assigned account manager

Each node writes a step log to OnboardingStep table.
The flow can be re-run from the last successful node if it fails mid-way.
"""
import json
import logging
import uuid
from datetime import datetime, timezone
from typing import TypedDict

import anthropic
from langgraph.graph import END, StateGraph

logger = logging.getLogger(__name__)


# ── State schema shared across all nodes ────────────────────────────────────

class OnboardingState(TypedDict):
    # Inputs
    flow_id: str
    agency_id: str
    client_id: str
    client_name: str
    client_email: str
    account_manager_name: str
    deal_notes: str             # raw notes from HubSpot deal

    # Outputs written by each node
    client_brief: str           # generated by node 1
    email_sent: bool            # set by node 2
    crm_updated: bool           # set by node 3
    am_notified: bool           # set by node 4

    # Error tracking
    errors: list[str]


# ── Node 1: Generate Client Brief ────────────────────────────────────────────

async def generate_brief_node(state: OnboardingState) -> OnboardingState:
    """
    Calls Claude to convert raw HubSpot deal notes into a
    structured onboarding brief for the account manager.
    """
    from app.config import settings
    from app.database import AsyncSessionLocal
    from app.modules.m2_operations.models import OnboardingStep

    logger.info("[Onboarding] Generating brief for client %s", state["client_name"])

    brief = ""
    node_status = "success"
    error_msg = None

    try:
        client = anthropic.Anthropic(api_key=settings.anthropic_api_key)

        prompt = f"""You are an onboarding specialist at a digital marketing agency.

A new client has just signed. Here are the deal notes from our CRM:

Client: {state["client_name"]}
Email: {state["client_email"]}
Deal Notes: {state["deal_notes"] or "No notes provided"}

Write a structured onboarding brief for the account manager. Include:
1. Client Overview (2-3 sentences about who they are and what they need)
2. Key Goals (bullet points from the deal notes)
3. Immediate Actions Required (what the AM should do in week 1)
4. Potential Challenges (anything to watch out for)

Keep it concise and actionable. Use plain text, no markdown."""

        response = client.messages.create(
            model="claude-haiku-4-5",
            max_tokens=600,
            messages=[{"role": "user", "content": prompt}],
        )
        brief = response.content[0].text.strip()

    except Exception as e:
        error_msg = str(e)
        node_status = "failed"
        state["errors"].append(f"generate_brief: {error_msg}")
        logger.error("[Onboarding] Brief generation failed: %s", e)

    # Write step log
    async with AsyncSessionLocal() as db:
        step = OnboardingStep(
            flow_id=uuid.UUID(state["flow_id"]),
            step_name="generate_brief",
            status=node_status,
            output=brief[:500] if brief else None,
            error=error_msg,
        )
        db.add(step)

        # Update flow record
        from app.modules.m2_operations.models import OnboardingFlow
        from sqlalchemy import select
        flow_result = await db.execute(
            select(OnboardingFlow).where(
                OnboardingFlow.id == uuid.UUID(state["flow_id"])
            )
        )
        flow = flow_result.scalar_one_or_none()
        if flow:
            flow.client_brief = brief
            flow.last_completed_node = "generate_brief"
            flow.status = "in_progress"

        await db.commit()

    state["client_brief"] = brief
    return state


